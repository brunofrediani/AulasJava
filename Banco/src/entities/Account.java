package entities;

	public class Account {

		private int number;
		private String holder;
		private double balance;
		
		/* gerado somente getter para balance e number da conta. Esses parametrôs não devem ser acessados
		 * por motivos de segurança.Para que ninguém altere o balance(saldo) .
		 */
		
		
		
		public Account(int number, String holder) {
			this.number = number;
			this.holder = holder;
		}

		public Account(int number, String holder, double initialDeposit) {
			this.number = number;
			this.holder = holder;
			deposit(initialDeposit);
			
	/* Aqui foi trocado o this.balance = balance, pelo initialDeposit.Utilizamos a operação ao invés
	 * de usar a variável em si. dá mais segurança e caso mude a operação de deposito não vai ser 
	 * necessário mudar aqui
	 */
			
			
		}

		public int getNumber() {
			return number;
		}

		public String getHolder() {
			return holder;
		}

		public void setHolder(String holder) {
			this.holder = holder;
		}

		public double getBalance() {
			return balance;
		}

		public void deposit(double amount) {
			balance += amount;
		}
		
		public void withdraw(double amount) {
			balance -= amount + 5.0;
		}
		
		public String toString() {
			return "Account "
					+ number
					+ ", Holder: "
					+ holder
					+ ", Balance: $ "
					+ String.format("%.2f", balance);
		}
}